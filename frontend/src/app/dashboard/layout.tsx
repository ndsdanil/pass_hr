function DashboardSidebar() {
  const pathname = usePathname();
  
  const isActive = (href: string) => {
    if (href === '/dashboard') {
      return pathname === '/dashboard';
    }
    return pathname.startsWith(href);
  };

  return (
    <aside className="w-64 bg-gray-900 border-r border-gray-700 h-[calc(100vh-64px)] sticky top-16 overflow-y-auto">
      <div className="p-4">
        <h2 className="text-xl font-semibold text-white">Dashboard</h2>
        <p className="text-sm text-gray-400 mt-1">Job Seeker</p>
      </div>
      <nav className="mt-4">
        <ul>
          {navigation.map((item) => (
            <li key={item.href} className="mb-1">
              <Link 
                href={item.href}
                className={`flex items-center px-4 py-2 transition-colors duration-200 ${
                  isActive(item.href)
                    ? 'bg-orange-500/20 text-orange-400 font-medium'
                    : 'text-gray-300 hover:bg-gray-800 hover:text-orange-400'
                }`}
              >
                <span className="mr-3"><item.icon /></span>
                {item.name}
              </Link>
            </li>
          ))}
        </ul>
      </nav>
    </aside>
  );
}

